console.log("Data Types");

// Data Types - Primitive
// 1. numbers - whole, decimals, neg 
let age = 25;
console.log("Age: " + age);
console.log(typeof age);    // print out as 'number' in console

// 2. strings = ' ' or  " "
let name = "Tony";
console.log(typeof name);   // print out as 'string' in console

// 3. boolean
let isStudent = true;
console.log(isStudent);     // print out as 'true' in console

